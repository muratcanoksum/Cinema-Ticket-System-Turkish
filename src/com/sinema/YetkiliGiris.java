    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        hata_mesaj.setText("");

        String kullanici_adi = this.kullanici_adi.getText();
        String parola = new String(password.getPassword());
        yetkili_adi_gonder = kullanici_adi;
        boolean girisbasarili = islemler.girisYap(kullanici_adi, parola);

        if (girisbasarili) {
            YetkiliPanel yetkiliEkrani = new YetkiliPanel();
            setVisible(false);
            yetkiliEkrani.setVisible(true);
        } else {
            hata_mesaj.setText("Giriş Başarısız...Lütfen tekrar deneyin.");

        }
    }//GEN-LAST:event_jButton1ActionPerformed

