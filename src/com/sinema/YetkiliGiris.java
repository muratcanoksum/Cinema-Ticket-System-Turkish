    // password textfield içerisinde "ENTER" tuşuna bastığımızda 
    // jButton1ActionPerformed fonksiyonu ile aynı işlevi yapar.
    
    private void passwordKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_passwordKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            hata_mesaj.setText("");

            String kullanici_adi = this.kullanici_adi.getText();
            String parola = new String(password.getPassword());
            yetkili_adi_gonder = kullanici_adi;
            boolean girisbasarili = islemler.girisYap(kullanici_adi, parola);

            if (girisbasarili) {
                YetkiliPanel yetkiliEkrani = new YetkiliPanel();
                setVisible(false);
                yetkiliEkrani.setVisible(true);
            } else {
                hata_mesaj.setText("Giriş Başarısız...Lütfen tekrar deneyin.");

            }
        }
    }//GEN-LAST:event_passwordKeyPressed

    // şifre sıfırlamak için daha önce belirlenmiş Database içerisinde saklanan static final string
    // değerini kontrol eder doğru ise mysql veritabını içerisindeki tüm tabloları silip yenilerini açar.
    
    private void veri_onaylaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_veri_onaylaActionPerformed
        String sifre = new String(veri_sifirla.getPassword());
        if (sifre.equals(Database.sifirlasifre)) {
            veri_sifirla.setVisible(false);
            veri_onayla.setVisible(false);
            sifirla_text.setVisible(false);
            veri_sifirla.setText("");
            islemler.veritabaniSifirla();
            JOptionPane.showMessageDialog(null, "Veritabanı sıfırlama işlemi başarıyla tamamlandı.", "Veritabanı Sıfırlama Sistemi", 1);
        } else {
            veri_sifirla.setText("");
            JOptionPane.showMessageDialog(null, "Veritabanı sıfırlama şifreniz  yanlış", "Veritabanı Sıfırlama Sistemi", 1);
        }

    }//GEN-LAST:event_veri_onaylaActionPerformed

