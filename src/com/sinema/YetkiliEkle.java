    private void yetkili_guncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_yetkili_guncelleActionPerformed
        String ad = yetkili_adi.getText();
        String sifre = yetkili_sifre.getText();
        String kimlik = yetkili_kimlik.getText();
        String dogum = yetkili_dogum.getText();

        int selectedrow = admin_tablosu.getSelectedRow();

        if (selectedrow == -1) {

            if (model.getRowCount() == 0) {
                hata.setText("Yetkili Tablosu şu anda boş. ");
            } else {
                hata.setText("Lütfen güncellenecek bir yetkili seçin.");
            }
        } else {

            int id = (int) model.getValueAt(selectedrow, 0);
            islemler.yetkiliGuncelle(id, ad, sifre, kimlik, dogum);

            listeGoruntule();
            hata.setText("Yetkili başarıyla güncellendi...");
        }


    }//GEN-LAST:event_yetkili_guncelleActionPerformed

    private void yetkili_silActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_yetkili_silActionPerformed
        hata.setText("");

        int selectedrow = admin_tablosu.getSelectedRow();
        if (selectedrow == -1) {
            if (model.getRowCount() == 0) {
                hata.setText("Yetkili tablosu şu anda boş...");
            } else {
                hata.setText("Lütfen silinecek bir yetkili seçin...");
            }

        } else {
            int id = (int) model.getValueAt(selectedrow, 0);
            islemler.yetkiliSil(id);
            listeGoruntule();
            temizleVeri();
            hata.setText("Yetkili başarıyla silindi...");
        }
    }//GEN-LAST:event_yetkili_silActionPerformed

    private void yetkili_ekleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_yetkili_ekleActionPerformed
        hata.setText("");

        String ad = yetkili_adi.getText();
        String sifre = yetkili_sifre.getText();
        String kimlik = yetkili_kimlik.getText();
        String dogum = yetkili_dogum.getText();

        islemler.yetkiliEkle(ad, sifre, kimlik, dogum);
        listeGoruntule();
        yetkili_adi.setText("");
        yetkili_sifre.setText("");
        yetkili_kimlik.setText("");
        yetkili_dogum.setText("");
        temizleVeri();
        hata.setText("Yeni Yetkili Başarıyla Eklendi...");
    }//GEN-LAST:event_yetkili_ekleActionPerformed

