    public void listeGoruntule() {

        model.setRowCount(0);

        ArrayList<Yetkili> yetkililer = new ArrayList<Yetkili>();

        yetkililer = islemler.yetkiliGetir();

        if (yetkililer != null) {

            for (Yetkili yetki : yetkililer) {
                Object[] eklenecek = {yetki.getId(), yetki.getAd(), yetki.getPassword(), yetki.getKimlik(), yetki.getDogum()};

                model.addRow(eklenecek);

            }

        }

    }

    public void temizleVeri() {
        yetkili_adi.setText("");
        yetkili_sifre.setText("");
        yetkili_dogum.setText("");
        yetkili_kimlik.setText("");
    }

